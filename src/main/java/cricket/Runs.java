package cricket;

public class Runs {
	private int batsman;
	private int extras;
	private int total;
	private int non_boundary;
	public int getBatsman() {
		return batsman;
	}
	public void setBatsman(int batsman) {
		this.batsman = batsman;
	}
	public int getExtras() {
		return extras;
	}
	public void setExtras(int extras) {
		this.extras = extras;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getNon_boundary() {
		return non_boundary;
	}
	public void setNon_boundary(int non_boundary) {
		this.non_boundary = non_boundary;
	}
}

